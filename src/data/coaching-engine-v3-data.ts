export function getToneByEmotionAndSuit(
  emotionKey: string,
  suit: string,
  intensity: EmotionIntensity
): string {
  const suitKey: SuitToneKey = suit === 'major' ? 'pentacles' : (suit as SuitToneKey);
  const key = `${emotionKey}:${suitKey}:${intensity}`;
  return TONE_MATRIX[key]?.[0] ?? '지금은 천천히 마음을 정리하면서 대화 준비를 해보자.';
}
