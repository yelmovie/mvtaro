// Helper function to get card image path
export function getCardImagePath(card: typeof tarotCards78Data.cards[0]): string {
  const fileNumber = String(card.number).padStart(2, '0');
  const fileName = card.fileName.toLowerCase().replace(/ /g, ' ');
  return `figma:asset/${fileName}.png`;
}

// Helper to get all card IDs
export function getAllCardIds(): string[] {
  return tarotCards78Data.cards.map(card => card.id);
}

// Helper to get cards by suit
export function getCardsBySuit(suit: 'major' | 'wands' | 'cups' | 'swords' | 'pentacles') {
  return tarotCards78Data.cards.filter(card => card.suit === suit);
}
